/*Você precisa fazer um programa para converter a distância percorrida de embarcações, em relação ao porto de origem, de quilômetros para milhas náuticas. A conversão será aplicada aos dados de várias embarcações.

Seu programa deverá ler três números reais em cada linha: a distância atual da embarcação em relação ao porto de origem (em quilômetros), a velocidade média da embarcação (em quilômetros por hora), e uma quantidade de tempo (em horas) gasto para percorrer um trajeto.

Seu programa deve conter uma função que recebe os três valores e retorna a previsão da distância da embarcação em relação à origem ao final do tempo indicado.

O programa principal dever ler os dados de cada embarcação e escrever a distância atual em milhas náuticas e a previsão de distância total percorrida após o tempo indicado, na velocidade indicada. O programa irá terminar quando for digitado um valor negativo para a distância atual. Note que não há velocidade e nem distância a ser lida após um valor negativo de distância atual.

1 milha náutica = 1,852 quilômetros

O valor do tempo se refere a horas. Por exemplo, 2,5 horas são duas horas e meia.

Entradas:

Dados referentes a várias embarcações, cada qual:
Distância atual (km),
velocidade (km/h) e
tempo (horas) (são 3 números reais, numa mesma linha).
Saídas:

Distância atual em milhas (número real),
previsão de distância em milhas após continuar o deslocamento pelo tempo indicado, na velocidade indicada (número real).
Exemplo de entrada:

15 35 2
8.6 79.6 3.2
81 4.3 5
-1
Exemplo de saída:

8.1 45.9
4.64 142.18
43.74 55.35*/